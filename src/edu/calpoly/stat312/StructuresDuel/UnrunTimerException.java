package edu.calpoly.stat312.StructuresDuel;

public class UnrunTimerException extends RuntimeException {

	public UnrunTimerException() {
		super();
	}

	public UnrunTimerException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnrunTimerException(String message) {
		super(message);
	}

	public UnrunTimerException(Throwable cause) {
		super(cause);
	}

}
